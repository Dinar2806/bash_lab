#!/bin/bash
# Лабораторная работа №5. Вариант 19.
# Подсчет числа строк в исходных файлах (*.h, *.cpp)

# 1. ИНИЦИАЛИЗАЦИЯ ПЕРЕМЕННЫХ
# Задаем значения по умолчанию ДО начала логики
DIR=""
TOTAL_LINES=0
FILE_COUNT=0
HELP_FLAG=false

# 2. ОБРАБОТКА ПАРАМЕТРОВ
# Если передан флаг справки
if [[ "$1" == "?"  "$1" == "-h"  "$1" == "--help" ]]; then
    HELP_FLAG=true
fi

# УСЛОВИЕ 3: Вывод справки
if [[ "$HELP_FLAG" == true ]]; then
    cat << EOF
Описание: Рекурсивный подсчет строк в файлах *.h и *.cpp
Использование: $(basename "$0") [каталог]
Параметры:
  каталог  - путь к целевой директории
Примеры:
  $(basename "$0") /home/user/project
  $(basename "$0") .
EOF
    exit 0
fi

# УСЛОВИЕ 1: Интерактивный запрос, если параметры опущены
if [[ -z "$1" ]]; then
    read -p "Введите путь к директории для поиска [по умолчанию './']: " INPUT_DIR
    # Если пользователь просто нажал Enter, используем точку
    DIR="${INPUT_DIR:-.}"
else
    # Иначе берем первый аргумент
    DIR="$1"
fi

# 3. ВАЛИДАЦИЯ
# УСЛОВИЕ 2: Проверка существования директории
if [[ ! -d "$DIR" ]]; then
    echo "Ошибка: '$DIR' не является существующей директорией." >&2
    echo "# Синтаксис использования:" >&2
    echo "#   $(basename "$0") [путь_к_директории]" >&2
    exit 1
fi

echo "Сканирование директории: $(realpath "$DIR")"
echo "--------------------------------------------------"

# 4. ОСНОВНАЯ ЛОГИКА
# Безопасный обход файлов с пробелами в именах
while IFS= read -r -d '' file; do
    # Подсчет строк (используем awk для надежности, если нет переноса в конце файла)
    lines=$(awk 'END {print NR}' "$file" 2>/dev/null || echo 0)
    
    printf "%6d  %s\n" "$lines" "$file"
    
    # Агрегация данных
    TOTAL_LINES=$((TOTAL_LINES + lines))
    FILE_COUNT=$((FILE_COUNT + 1))
done < <(find "$DIR" -type f \( -name "*.h" -o -name "*.cpp" \) -print0 2>/dev/null)

echo "--------------------------------------------------"
if [[ $FILE_COUNT -eq 0 ]]; then
    echo "Файлы *.h и *.cpp не найдены."
else
    echo "Найдено файлов: $FILE_COUNT"
fi
echo "Суммарное количество строк: $TOTAL_LINES"