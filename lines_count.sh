#!/bin/bash

# Лабораторная работа №21. Вариант 19.
# Подсчет числа строк в исходных файлах (*.h, *.cpp) в директории и подкаталогах
# с вычислением суммарной длины (в строках).


# УСЛОВИЕ 3: Если указан параметр "?", выводится подсказка (справка)
if [[ "$1" == "?" || "$1" == "-h" || "$1" == "--help" ]]; then
    cat << EOF
(УСЛ. 3)
Описание: Рекурсивный подсчет строк в файлах *.h и *.cpp
Использование: $(basename "$0") [каталог]
Параметры:
  каталог  - путь к целевой директории
             (если параметр опущен, путь запрашивается интерактивно)
Примеры:
  $(basename "$0") /home/user/project
  $(basename "$0") .
  $(basename "$0") "?"
EOF
    exit 0
fi

# УСЛОВИЕ 1: Если параметры опущены, их значения запрашиваются у пользователя
if [[ -z "$1" ]]; then
    read -p "Введите путь к директории для поиска (УСЛ. 1) [по умолчанию './']: " DIR
    DIR="${DIR:-.}"
else
    DIR="$1"
fi

# УСЛОВИЕ 2: При ошибке в задании параметров выводится комментированный синтаксис
if [[ ! -d "$DIR" ]]; then
    echo " Ошибка: '$DIR' не является существующей директорией." >&2
    echo "# Синтаксис использования (УСЛ. 2):" >&2
    echo "#   $(basename "$0") [путь_к_директории]" >&2
    echo "#   $(basename "$0") ?   # вывести справку" >&2
    exit 1
fi

echo " Сканирование директории: $(realpath "$DIR")"
echo "--------------------------------------------------"

total_lines=0
file_count=0

# Безопасный обход файлов с пробелами в именах (-print0 + read -d '')
while IFS= read -r -d '' file; do
    # Подсчет строк в файле
    lines=$(wc -l < "$file" 2>/dev/null || echo 0)
    printf "%6d  %s\n" "$lines" "$file"
    total_lines=$((total_lines + lines))
    ((file_count++))
done < <(find "$DIR" -type f \( -name "*.h" -o -name "*.cpp" \) -print0 2>/dev/null)

echo "--------------------------------------------------"
if [[ $file_count -eq 0 ]]; then
    echo " Файлы *.h и *.cpp не найдены."
else
    echo " Найдено файлов: $file_count"
fi
echo " Суммарное количество строк: $total_lines"
